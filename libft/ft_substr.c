#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	char	*substr;
	size_t	i;

	substr = (char *) malloc(len + 1);
	if (substr == NULL || s == NULL)
		return (NULL);
	if (start >= ft_strlen(s))
	{
		substr[0] = 0;
		return (substr);
	}
	i = 0;
	while (i < len && s[start])
	{
		substr[i] = s[start];
		i++;
		start++;
	}
	substr[i] = '\0';
	return (substr);
}
